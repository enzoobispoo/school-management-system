const DashboardLanguageContext = createContext<DashboardLanguageContextValue | null>(
  null
);

export function DashboardLanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<DashboardLanguage>("pt-BR");

  useEffect(() => {
    try {
      const stored = window.localStorage.getItem(STORAGE_KEY);
      if (stored === "pt-BR" || stored === "en" || stored === "es") {
        setLanguage(stored);
      }
    } catch {
      /* noop */
    }
  }, []);

  useEffect(() => {
    try {
      window.localStorage.setItem(STORAGE_KEY, language);
    } catch {
      /* noop */
    }
    document.documentElement.lang = language;
  }, [language]);

  const value = useMemo<DashboardLanguageContextValue>(
    () => ({
      language,
      setLanguage,
      t: (key: string, vars?: DashboardTranslateVars) => {
        const raw =
          DASHBOARD_TRANSLATIONS[language]?.[key] ??
          DASHBOARD_TRANSLATIONS["pt-BR"]?.[key] ??
          key;
        return applyTemplate(raw, vars);
      },
    }),
    [language]
  );

  return (
    <DashboardLanguageContext.Provider value={value}>
      {children}
    </DashboardLanguageContext.Provider>
  );
}

export function useDashboardLanguage() {
  const ctx = useContext(DashboardLanguageContext);
  if (!ctx) {
    throw new Error(
      "useDashboardLanguage must be used within DashboardLanguageProvider"
    );
  }
  return ctx;
}
