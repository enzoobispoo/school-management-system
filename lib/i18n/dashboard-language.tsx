export type DashboardLanguage = "pt-BR" | "en" | "es";

export type DashboardTranslateVars = Record<string, string | number>;

function applyTemplate(
  template: string,
  vars?: DashboardTranslateVars
): string {
  if (!vars) return template;
  let out = template;
  for (const [k, v] of Object.entries(vars)) {
    out = out.split(`{{${k}}}`).join(String(v));
  }
  return out;
}

/** BCP 47 tag for `Intl` / `toLocaleDateString`. */
export function dashboardLocaleTag(language: DashboardLanguage): string {
  if (language === "en") return "en-US";
  if (language === "es") return "es";
  return "pt-BR";
}

